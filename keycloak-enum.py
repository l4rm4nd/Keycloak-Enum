def cmd_fingerprint(args) -> int:
    db_path = Path(args.db)
    db = load_db(db_path)
    if not db:
        print(f"ERROR: fingerprint database not found at {db_path}", file=sys.stderr)
        print("Build one with:  python3 keycloak-enum.py collect --all", file=sys.stderr)
        return 1

    index = build_index(db)
    result = fingerprint_target(
        args.url, index,
        verify=not args.no_verify,
        timeout=args.timeout,
        verbose=args.verbose,
    )

    W = 12  # label width
    print()
    print(f"{'Target':{W}}: {result['target']}")
    if result.get("console_url"):
        print(f"{'Console':{W}}: {result['console_url']}")
    kc_detected = result.get("detected", False)
    print(f"{'Keycloak':{W}}: {'Detected' if kc_detected else 'Not detected'}")

    if not kc_detected:
        print(f"\n{result.get('explanation', '')}")
        return 0

    print()
    if result.get("first_js"):
        print(f"{'JavaScript':{W}}: {result['first_js']}")
    if result.get("first_css"):
        print(f"{'CSS':{W}}: {result['first_css']}")

    status = result.get("status", "unknown")
    versions = result.get("versions", [])
    confidence = result.get("confidence", "unknown")
    matched = result.get("matched", 0)
    total = result.get("hashes_checked", 0)

    status_label = {
        "exact_unique":    "exact_unique",
        "exact_ambiguous": "exact_ambiguous",
        "partial":         "partial",
        "unknown":         "unknown",
    }.get(status, status)

    print()
    print(f"{'Match':{W}}: {status_label}")
    print(f"{'Confidence':{W}}: {confidence}")
    if versions:
        label = "Version" if len(versions) == 1 else "Version(s)"
        print(f"{label:{W}}: {', '.join(versions)}")
    print()
    print(result.get("explanation", ""))

    if args.json_output:
        print()
        print(json.dumps(result, indent=2))

    return 0


def cmd_collect(args) -> int:
    db_path = Path(args.db)
    db = load_db(db_path)

    if args.all:
        versions = KNOWN_VERSIONS
    elif args.version:
        versions = [args.version]
    else:
        print("ERROR: specify a version or --all", file=sys.stderr)
        return 1

    print(f"Collecting {len(versions)} version(s) into {db_path}")
    ok = fail = 0
    for ver in versions:
        image = args.image if len(versions) == 1 else None
        if collect_version(ver, db, image=image, force=args.force):
            ok += 1
        else:
            fail += 1

    print(f"\nDone: {ok} succeeded, {fail} failed.")
    save_db(db, db_path)
    return 0 if fail == 0 else 1


def cmd_export_db(args) -> int:
    """Import an existing SQLite fingerprint database into fingerprints.json."""
    try:
        import sqlite3
    except ImportError:
        print("ERROR: sqlite3 not available", file=sys.stderr)
        return 1

    sqlite_path = Path(args.sqlite)
    if not sqlite_path.exists():
        print(f"ERROR: {sqlite_path} not found", file=sys.stderr)
        return 1

    conn = sqlite3.connect(str(sqlite_path))
    rows = conn.execute(
        "SELECT b.version, a.sha256 FROM assets a JOIN builds b ON a.build_id = b.id"
    ).fetchall()
    conn.close()

    db: dict[str, list[str]] = {}
    for version, sha256 in rows:
        db.setdefault(version, []).append(sha256)

    db_path = Path(args.db)
    save_db(db, db_path)
    print(f"Exported {len(rows)} records for {len(db)} versions from {sqlite_path}")
    return 0



