def _list_jars_in_container(container_id: str) -> list[str]:
    """Return paths of all JAR files inside *container_id* (one docker export)."""
    docker_proc = subprocess.Popen(
        ["docker", "export", container_id],
        stdout=subprocess.PIPE, stderr=subprocess.PIPE,
    )
    tar_proc = subprocess.Popen(
        ["tar", "-t"],
        stdin=docker_proc.stdout,
        stdout=subprocess.PIPE, stderr=subprocess.PIPE,
    )
    docker_proc.stdout.close()  # allow docker_proc to receive SIGPIPE if tar exits
    out, _ = tar_proc.communicate()
    docker_proc.wait()
    lines = out.decode("utf-8", errors="replace").splitlines()
    return ["/" + l.lstrip("./") for l in lines if l.endswith(".jar")]


def _pick_jar(jar_list: list[str], grep_term: str, exclude: str | None = None) -> str | None:
    hits = [p for p in jar_list if grep_term in p]
    if exclude:
        hits = [p for p in hits if exclude not in p]
    return hits[0] if hits else None


def _copy_jar(container_id: str, jar_path: str) -> bytes | None:
    """Copy a JAR from a container to memory. Returns bytes or None."""
    with tempfile.NamedTemporaryFile(suffix=".jar", delete=False) as tmp:
        tmp_path = Path(tmp.name)
    try:
        r = subprocess.run(
            ["docker", "cp", f"{container_id}:{jar_path}", str(tmp_path)],
            capture_output=True,
        )
        return tmp_path.read_bytes() if r.returncode == 0 else None
    except Exception:
        return None
    finally:
        tmp_path.unlink(missing_ok=True)


def _hash_vite_jar(jar_bytes: bytes, manifest_path: str, resources_dir: str) -> list[str]:
    """Hash all Vite JS/CSS assets in an admin-ui or account-ui JAR."""
    hashes = []
    with zipfile.ZipFile(io.BytesIO(jar_bytes)) as zf:
        names = set(zf.namelist())
        if manifest_path in names:
            # Vite build (KC 24+): use manifest for exact asset enumeration
            manifest: dict = json.loads(zf.read(manifest_path).decode("utf-8"))
            seen: set[str] = set()
            for meta in manifest.values():
                for rel in [meta.get("file", "")] + meta.get("css", []):
                    if not rel:
                        continue
                    ext = rel.rsplit(".", 1)[-1].lower()
                    if ext not in ("js", "css"):
                        continue
                    full = resources_dir + rel
                    if full not in seen and full in names:
                        seen.add(full)
                        hashes.append(sha256hex(zf.read(full)))
        else:
            # Pre-Vite (Webpack era, KC ≤23): hash all JS/CSS under resources
            assets_prefix = resources_dir + "assets/"
            for name in names:
                if name.startswith(assets_prefix) and name.endswith((".js", ".css")):
                    hashes.append(sha256hex(zf.read(name)))
    return hashes


def _hash_theme_files(jar_bytes: bytes, file_set: frozenset[str]) -> list[str]:
    """Hash specific named files from a themes JAR."""
    hashes = []
    with zipfile.ZipFile(io.BytesIO(jar_bytes)) as zf:
        names = set(zf.namelist())
        for path in file_set:
            if path in names:
                hashes.append(sha256hex(zf.read(path)))
    return hashes


def collect_version(
    version: str,
    db: dict[str, list[str]],
    *,
    image: str | None = None,
    force: bool = False,
) -> bool:
    """Collect fingerprints for one Keycloak version. Updates *db* in place."""
    if version in db and not force:
        print(f"  {version}: already in database, skipping (use --force to redo)")
        return True

    img = image or KC_IMAGE.format(version=version)
    sys.stdout.write(f"  {version}  ({img}) …")
    sys.stdout.flush()

    # Pull image quietly in case it's not cached
    subprocess.run(["docker", "pull", "-q", img], capture_output=True)

    r = subprocess.run(["docker", "create", img], capture_output=True, text=True)
    if r.returncode != 0:
        print(f"  FAILED — could not create container: {r.stderr.strip()}")
        return False

    container_id = r.stdout.strip()
    all_hashes: list[str] = []

    try:
        # Get the full JAR file list with a single docker export
        sys.stdout.write(" listing…")
        sys.stdout.flush()
        jar_paths = _list_jars_in_container(container_id)

        # Admin-ui and account-ui JARs (Vite SPAs)
        for grep_term, manifest_path, resources_dir, label in _JAR_TARGETS:
            jar_path = _pick_jar(jar_paths, grep_term)
            if not jar_path:
                sys.stdout.write(f" [{label}:–]")
                sys.stdout.flush()
                continue
            jar_bytes = _copy_jar(container_id, jar_path)
            if not jar_bytes:
                continue
            h = _hash_vite_jar(jar_bytes, manifest_path, resources_dir)
            all_hashes.extend(h)
            sys.stdout.write(f" [{label}:{len(h)}]")
            sys.stdout.flush()

        # Login-theme CSS/JS from keycloak-themes.jar
        # Also picks up vendor JS for ≤26.0.x (before the vendor JAR was split out)
        themes_path = _pick_jar(jar_paths, "keycloak-themes", exclude="keycloak-themes-vendor")
        if themes_path:
            jar_bytes = _copy_jar(container_id, themes_path)
            if jar_bytes:
                h = _hash_theme_files(jar_bytes, _LOGIN_THEME_FILES)
                all_hashes.extend(h)
                sys.stdout.write(f" [themes:{len(h)}]")
                sys.stdout.flush()

        # Vendor JAR: keycloak-themes-vendor.jar (introduced in 26.1.0)
        vendor_path = _pick_jar(jar_paths, "keycloak-themes-vendor")
        if vendor_path:
            jar_bytes = _copy_jar(container_id, vendor_path)
            if jar_bytes:
                h = _hash_theme_files(jar_bytes, _THEMES_VENDOR_FILES)
                all_hashes.extend(h)
                sys.stdout.write(f" [vendor:{len(h)}]")
                sys.stdout.flush()

        unique = list(set(all_hashes))
        db[version] = unique
        print(f"  →  {len(unique)} hashes ✓")
        return True

    except Exception as e:
        print(f"  ERROR: {e}")
        return False
    finally:
        subprocess.run(["docker", "rm", container_id], capture_output=True)


