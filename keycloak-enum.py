def main() -> int:
    parser = argparse.ArgumentParser(
        prog="keycloak-fingerprint",
        description="Detect Keycloak version",
        formatter_class=argparse.RawDescriptionHelpFormatter,
    )
    parser.add_argument(
        "--db", default=str(DEFAULT_DB), metavar="PATH",
        help="Path to fingerprints.json  [%(default)s]",
    )
    parser.add_argument("--version", action="version", version=f"%(prog)s {TOOL_VERSION}")

    sub = parser.add_subparsers(dest="command", required=True)

    # ── fingerprint ──────────────────────────────────────────────────────────
    fp = sub.add_parser("fingerprint", aliases=["fp"],
                        help="Probe a live Keycloak instance and identify its version")
    fp.add_argument("url", help="Target URL  (e.g. https://keycloak.example.com)")
    fp.add_argument("--no-verify", action="store_true",
                    help="Disable TLS certificate verification")
    fp.add_argument("--timeout", type=float, default=15, metavar="SECS",
                    help="HTTP timeout in seconds  [%(default)s]")
    fp.add_argument("--verbose", "-v", action="store_true",
                    help="Print debug output to stderr")
    fp.add_argument("--json", dest="json_output", action="store_true",
                    help="Also print the full result as JSON")

    # ── collect ──────────────────────────────────────────────────────────────
    col = sub.add_parser("collect",
                         help="Collect fingerprints from a Keycloak Docker image (needs docker)")
    col.add_argument("version", nargs="?", metavar="VERSION",
                     help="Keycloak version to collect  (e.g. 26.7.1)")
    col.add_argument("--all", action="store_true",
                     help="Collect all versions in the built-in list")
    col.add_argument("--force", action="store_true",
                     help="Re-collect even if already in the database")
    col.add_argument("--image", metavar="IMAGE",
                     help="Custom Docker image  (single-version only)")

    # ── export-db ────────────────────────────────────────────────────────────
    exp = sub.add_parser("export-db",
                         help="Import from an existing keycloak-fingerprint SQLite database")
    exp.add_argument("sqlite", metavar="SQLITE_FILE",
                     help="Path to fingerprints.sqlite  (e.g. data/fingerprints.sqlite)")

    args = parser.parse_args()

    dispatch = {
        "fingerprint": cmd_fingerprint,
        "fp":          cmd_fingerprint,
        "collect":     cmd_collect,
        "export-db":   cmd_export_db,
    }
    return dispatch[args.command](args)


if __name__ == "__main__":
    sys.exit(main())
