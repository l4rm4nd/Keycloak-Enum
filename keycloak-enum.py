def http_get(url: str, *, verify: bool = True, timeout: float = 15) -> tuple[bytes, int]:
    """GET *url*, return (body_bytes, http_status_code). Raises on network error."""
    ctx = ssl.create_default_context()
    if not verify:
        ctx.check_hostname = False
        ctx.verify_mode = ssl.CERT_NONE
    opener = urllib.request.build_opener(
        urllib.request.HTTPSHandler(context=ctx),
        urllib.request.HTTPRedirectHandler(),
    )
    req = urllib.request.Request(url, headers={"User-Agent": USER_AGENT})
    try:
        with opener.open(req, timeout=timeout) as resp:
            return resp.read(), resp.status
    except urllib.error.HTTPError as e:
        try:
            return e.read(), e.code
        except Exception:
            return b"", e.code


# ─────────────────────────────────────────────────────────────────────────────
# Hashing
# ─────────────────────────────────────────────────────────────────────────────

def sha256hex(data: bytes) -> str:
    return hashlib.sha256(data).hexdigest()


# ─────────────────────────────────────────────────────────────────────────────
# Progress bar
# ─────────────────────────────────────────────────────────────────────────────

_PROGRESS_LOCK = threading.Lock()


def _progress(done: int, total: int, label: str = "chunks") -> None:
    """Overwrite the current stderr line with a progress bar (TTY only)."""
    if not sys.stderr.isatty():
        return
    width = 28
    filled = int(width * done / total) if total else width
    bar = "\u2588" * filled + "\u2591" * (width - filled)
    with _PROGRESS_LOCK:
        sys.stderr.write(f"\r  [{bar}] {done}/{total}  {label}  ")
        sys.stderr.flush()


def _clear_progress() -> None:
    """Erase the progress bar line from stderr."""
    if not sys.stderr.isatty():
        return
    with _PROGRESS_LOCK:
        sys.stderr.write(f"\r{' ' * 70}\r")
        sys.stderr.flush()


# ─────────────────────────────────────────────────────────────────────────────
# Fingerprint database  (fingerprints.json)
# ─────────────────────────────────────────────────────────────────────────────

def load_db(path: Path) -> dict[str, list[str]]:
    """Load fingerprints.json → {version: [sha256, ...]}"""
    if not path.exists():
        return {}
    with open(path) as f:
        raw = json.load(f)
    return {k: v for k, v in raw.items() if not k.startswith("_")}


def save_db(db: dict[str, list[str]], path: Path) -> None:
    """Save {version: [sha256, ...]} to fingerprints.json."""
    total = sum(len(v) for v in db.values())
    out: dict = {
        "_meta": {
            "generated": str(date.today()),
            "tool": f"keycloak-fingerprint/{TOOL_VERSION}",
            "versions": len(db),
            "total_hashes": total,
        }
    }
    out.update({k: sorted(set(v)) for k, v in sorted(db.items())})
    with open(path, "w") as f:
        json.dump(out, f, separators=(",", ":"))
        f.write("\n")
    print(f"Saved {len(db)} versions, {total} hashes → {path}")


def build_index(db: dict[str, list[str]]) -> dict[str, list[str]]:
    """Build inverse lookup: sha256 → [version, ...] for O(1) matching."""
    idx: dict[str, list[str]] = defaultdict(list)
    for ver, hashes in db.items():
        for h in hashes:
            idx[h].append(ver)
    return dict(idx)


# ─────────────────────────────────────────────────────────────────────────────
# HTML parsing helpers
# ─────────────────────────────────────────────────────────────────────────────

_RE_RESOURCE_HASH = re.compile(r"/(?:auth/)?resources/([a-zA-Z0-9]{3,12})/")
_RE_SCRIPT_SRC = re.compile(r'<script[^>]+src=["\']([^"\']+\.js[^"\']*)["\']', re.I)
_RE_LINK_HREF = re.compile(r'<link[^>]+href=["\']([^"\']+\.css[^"\']*)["\']', re.I)


def is_keycloak_page(html: str) -> bool:
    """Return True if *html* contains Keycloak resource URLs."""
    return bool(_RE_RESOURCE_HASH.search(html))


def extract_resource_hash(html: str) -> str | None:
    m = _RE_RESOURCE_HASH.search(html)
    return m.group(1) if m else None


def extract_asset_urls(html: str, origin: str) -> list[str]:
    """Return absolute Vite SPA entry asset URLs found in the HTML."""
    parsed = urllib.parse.urlparse(origin)
    host = f"{parsed.scheme}://{parsed.netloc}"
    urls = []
    for m in list(_RE_SCRIPT_SRC.finditer(html)) + list(_RE_LINK_HREF.finditer(html)):
        url = m.group(1)
        if url.startswith("//"):
            url = parsed.scheme + ":" + url
        elif url.startswith("/"):
            url = host + url
        elif not url.startswith("http"):
            url = origin.rstrip("/") + "/" + url
        # Only include Vite content-hashed SPA entry files (not vendor CSS, etc.)
        if "/assets/main-" in url or "/assets/index-" in url:
            urls.append(url)
    return urls


def resource_base_from_asset_url(asset_url: str) -> str | None:
    """Strip '/assets/<file>' suffix to get the Vite resource base URL."""
    idx = asset_url.find("/assets/")
    return asset_url[:idx] if idx != -1 else None


# ─────────────────────────────────────────────────────────────────────────────
# Vite manifest fetching
# ─────────────────────────────────────────────────────────────────────────────

def fetch_vite_hashes(
    resource_base: str,
    *,
    verify: bool = True,
    timeout: float = 15,
    workers: int = 20,
    show_progress: bool = False,
    label: str = "chunks",
) -> list[str]:
    """Fetch .vite/manifest.json and return sha256 hashes of all chunk JS/CSS.

    Asset downloads are parallelised with *workers* threads so that even
    200-chunk SPAs complete in a few seconds instead of minutes.
    """
    manifest_url = resource_base.rstrip("/") + "/.vite/manifest.json"
    try:
        body, status = http_get(manifest_url, verify=verify, timeout=timeout)
        if status != 200:
            return []
        manifest: dict = json.loads(body.decode("utf-8"))
    except Exception:
        return []

    assets_base = resource_base.rstrip("/") + "/assets/"
    seen: set[str] = set()
    urls: list[str] = []

    for meta in manifest.values():
        for rel in [meta.get("file", "")] + meta.get("css", []):
            if not rel:
                continue
            if rel.rsplit(".", 1)[-1].lower() not in ("js", "css"):
                continue
            url = assets_base + rel.split("/")[-1]
            if url not in seen:
                seen.add(url)
                urls.append(url)

    def _fetch_one(url: str) -> str | None:
        try:
            fbody, fstatus = http_get(url, verify=verify, timeout=timeout)
            if fstatus == 200 and fbody:
                return sha256hex(fbody)
        except Exception:
            pass
        return None

    hashes: list[str] = []
    total = len(urls)
    show = show_progress and total > 3
    done = 0

    with ThreadPoolExecutor(max_workers=min(workers, total or 1)) as pool:
        futures = {pool.submit(_fetch_one, url): url for url in urls}
        for future in as_completed(futures):
            done += 1
            if show:
                _progress(done, total, label)
            result = future.result()
            if result:
                hashes.append(result)

    if show:
        _clear_progress()

    return hashes


# ─────────────────────────────────────────────────────────────────────────────
# Vendor JS fetching
# ─────────────────────────────────────────────────────────────────────────────

def fetch_vendor_hashes(
    host: str,
    resource_hash: str,
    *,
    verify: bool = True,
    timeout: float = 15,
) -> list[str]:
    """Fetch login-theme vendor JS files, return sha256 hashes."""
    hashes = []
    for rel_path in _VENDOR_PATHS:
        for prefix in _PREFIXES:
            url = host + prefix + resource_hash + "/" + rel_path
            try:
                body, status = http_get(url, verify=verify, timeout=timeout)
                if status == 200 and body:
                    hashes.append(sha256hex(body))
                    break
            except Exception:
                pass
    return hashes


# ─────────────────────────────────────────────────────────────────────────────
# Hash matching
# ─────────────────────────────────────────────────────────────────────────────

def match_hashes(sha256s: list[str], index: dict[str, list[str]]) -> dict:
    """Intersect all hash lookups and return a match result dict."""
    total = len(sha256s)
    if not sha256s:
        return _mr("unknown", [], "unknown", "No assets downloaded.", 0, 0)

    version_sets: list[set[str]] = []
    matched = 0
    for h in sha256s:
        vs = set(index.get(h, []))
        if vs:
            version_sets.append(vs)
            matched += 1

    if not version_sets:
        return _mr(
            "unknown", [], "unknown",
            f"None of the {total} observed hashes matched the database.\n"
            "The version may not be in the database yet. Try: collect <version>",
            0, total,
        )

    # Intersect — the result is the set of versions that contain ALL matched hashes
    intersection = version_sets[0].copy()
    for s in version_sets[1:]:
        intersection &= s
        if not intersection:
            break

    candidates = sorted(intersection)

    if not candidates:
        # Individual hashes hit the DB but no single version contains all of them —
        # can happen with CDN caches or mixed builds.
        union = sorted(set().union(*version_sets))
        return _mr(
            "partial", union, "low",
            f"{matched}/{total} hashes found in database but share no common version.\n"
            f"Possible candidates: {', '.join(union[:6])}{'…' if len(union) > 6 else ''}",
            matched, total,
        )

    confidence = "high" if matched >= 5 else "medium"

    if len(candidates) == 1:
        return _mr(
            "exact_unique", candidates, confidence,
            f"{matched}/{total} hashes uniquely identify Keycloak {candidates[0]}.",
            matched, total,
        )

    return _mr(
        "exact_ambiguous", candidates, "medium" if matched >= 5 else "low",
        f"{matched}/{total} hashes matched {len(candidates)} versions: "
        f"{', '.join(candidates)}.\n"
        "Exact patch cannot be determined from the publicly available assets alone.",
        matched, total,
    )


def _mr(status, versions, confidence, explanation, matched, total):
    return dict(status=status, versions=versions, confidence=confidence,
                explanation=explanation, matched=matched, total=total)


# ─────────────────────────────────────────────────────────────────────────────
# Main fingerprinting flow
# ─────────────────────────────────────────────────────────────────────────────

def fingerprint_target(
    target: str,
    index: dict[str, list[str]],
    *,
    verify: bool = True,
    timeout: float = 15,
    verbose: bool = False,
) -> dict:
    """Probe *target* and return a result dict."""
    if not target.startswith(("http://", "https://")):
        target = "https://" + target
    target = target.rstrip("/")
    parsed = urllib.parse.urlparse(target)
    host = f"{parsed.scheme}://{parsed.netloc}"

    def dbg(msg: str) -> None:
        if verbose:
            print(f"  [*] {msg}", file=sys.stderr)

    hashes: list[str] = []
    console_url: str | None = None
    resource_hash: str | None = None
    first_js: str | None = None
    first_css: str | None = None
    detected = False

    # ── Step 1: Probe console / login paths ──────────────────────────────────
    for path in _PROBE_PATHS:
        url = host + path
        try:
            body, status = http_get(url, verify=verify, timeout=timeout)
        except Exception as e:
            dbg(f"{path} → network error: {e}")
            continue

        html = body.decode("utf-8", errors="replace")
        dbg(f"{path} → HTTP {status}, keycloak={is_keycloak_page(html)}")
        if not is_keycloak_page(html):
            continue

        detected = True
        console_url = url
        resource_hash = extract_resource_hash(html)
        dbg(f"Keycloak detected, resource_hash={resource_hash}")

        # ── Step 2: Fetch entry assets listed in HTML ─────────────────────
        for asset_url in extract_asset_urls(html, host):
            try:
                abody, astatus = http_get(asset_url, verify=verify, timeout=timeout)
                if astatus == 200 and abody:
                    h = sha256hex(abody)
                    hashes.append(h)
                    fname = asset_url.split("/")[-1]
                    if asset_url.endswith(".js") and not first_js:
                        first_js = fname
                    if asset_url.endswith(".css") and not first_css:
                        first_css = fname
                    dbg(f"  {fname} → {h[:16]}")

                    # ── Step 3: Vite manifest → all chunk files ───────────
                    resource_base = resource_base_from_asset_url(asset_url)
                    if resource_base:
                        chunk_hashes = fetch_vite_hashes(
                            resource_base, verify=verify, timeout=timeout,
                            show_progress=not verbose, label="chunks",
                        )
                        dbg(f"  Vite chunks: {len(chunk_hashes)} hashes")
                        hashes.extend(chunk_hashes)
            except Exception as e:
                dbg(f"  asset fetch failed: {e}")

        break  # stop after first Keycloak page found

    if not detected:
        return {
            "target": target,
            "detected": False,
            "explanation": "Keycloak not detected at any probed path.",
        }

    # ── Step 4: Vendor JS via login resource hash ─────────────────────────────
    if resource_hash:
        vendor_hashes = fetch_vendor_hashes(host, resource_hash, verify=verify, timeout=timeout)
        dbg(f"Vendor JS: {len(vendor_hashes)}/{len(_VENDOR_PATHS)} fetched")
        hashes.extend(vendor_hashes)

        # ── Step 5: Probe SPA Vite manifests via login resource hash ─────────
        # The account console (/realms/master/account/) often redirects to the
        # OIDC login page, so the SPA HTML is never served and Step 2 yields no
        # Vite chunks.  All /resources/<hash>/... paths are served publicly, so
        # we can fetch the manifest directly and get version-unique chunk hashes.
        # This resolves patch-level ambiguity within a minor version series
        # (e.g., 26.2.0 vs 26.2.5 which share all login-theme and vendor assets).
        # Both SPA subpaths are independent — probe them in parallel.
        def _probe_spa(spa_sub: str) -> tuple[str, list[str]]:
            lbl = spa_sub.split(".")[-1] + " chunks"  # "v3 chunks" / "v2 chunks"
            for prefix in _PREFIXES:
                spa_base = host + prefix + resource_hash + spa_sub
                chunk_hashes = fetch_vite_hashes(
                    spa_base, verify=verify, timeout=timeout,
                    show_progress=not verbose, label=lbl,
                )
                if chunk_hashes:
                    return spa_sub, chunk_hashes
            return spa_sub, []

        with ThreadPoolExecutor(max_workers=len(_SPA_SUBPATHS)) as pool:
            for spa_sub, chunk_hashes in pool.map(_probe_spa, _SPA_SUBPATHS):
                if chunk_hashes:
                    dbg(f"SPA manifest ({spa_sub.split('/')[-1]}): {len(chunk_hashes)} hashes")
                    hashes.extend(chunk_hashes)

    # ── Step 6: Match ─────────────────────────────────────────────────────────
    unique_hashes = list(dict.fromkeys(h for h in hashes if h))
    result = match_hashes(unique_hashes, index)

    return {
        "target": target,
        "console_url": console_url,
        "detected": True,
        "resource_hash": resource_hash,
        "first_js": first_js,
        "first_css": first_css,
        "hashes_checked": len(unique_hashes),
        **result,
    }


